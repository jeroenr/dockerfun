CREATE TABLE IBANX_ATTRIBUTE_CHANGE
(
    ID INTEGER NOT NULL,
    OID INTEGER,
    ATTRIBUTE_NAME VARCHAR2(254) NOT NULL,
    ORIGINAL_VALUE VARCHAR2(2500),
    NEW_VALUE VARCHAR2(2500),
    SEQUENCE INTEGER,
    PRIMARY KEY (ID)
);

CREATE INDEX I_IBANX_ATTR_CHANGE_OID ON IBANX_ATTRIBUTE_CHANGE (OID);
