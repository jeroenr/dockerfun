CREATE TABLE IBANX_ATTRIBUTE_CHANGE
(
    ID INTEGER NOT NULL,
    OID INTEGER,
    ATTRIBUTE_NAME VARCHAR(254) NOT NULL,
    ORIGINAL_VALUE MEDIUMTEXT NULL,
    NEW_VALUE MEDIUMTEXT NULL,
    SEQUENCE INTEGER,
    PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX I_IBANX_ATTR_CHANGE_ID ON IBANX_ATTRIBUTE_CHANGE (ID);

CREATE INDEX I_IBANX_ATTR_CHANGE_OID ON IBANX_ATTRIBUTE_CHANGE (OID);
